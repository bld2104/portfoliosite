      <div id="collapseOne" class="panel-collapse collapse">
      <div class="panel-body">
      <p>John Lee, a civil hacker with a Masters Degree in public health started this project at the SEPTA Public Transport Hackathon in September 2013. The goal was to put together a device that could ride on the SEPTA busses and would measure pollution in the air. The Climate Tracker project has its own website <a href="http://climatetracker.wordpress.com/about/" target="_blank">here.</a></p>
