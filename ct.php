<br><br>
      <p>During summer 2014, I participated in an independent study with Montgomery County Community College in conjunction with Code for Philly.  I worked with the Climate Tracker project team to code an arduino device that would collect data on air quality in the city of Philadelphia. </p>
      <h1> About the Project </h1>
      <p>The CAC Climate Tracker will create a city-wide network of sensors to gather data on microclimate and pollution and make the data available to researchers and app developers in order to study the effects of climate change and publicize heat and pollution data in real time. The data will be combined with OpenTreeMaps to study the effect trees have on pollution and their capacity to mitigate climate change at the local level, with the goal of putting a dollar value on a tree in terms of mitigating pollution and the effects of climate change.
         Full details on the project, and overall project progress can be found at: http://climatetracker.wordpress.com/about/ 
      </p>
