      <div class="panel panel-default">
      <div class="panel-heading">
      <h5 class="panel-title">
      <a data-toggle="collapse" data-parent="#accordion" href="#collapseFive">How is the data being stored? Where does it go after the device picks it up?</a>
      </h5>
      </div>
      <div id="collapseFive" class="panel-collapse collapse">
      <div class="panel-body">
      <p>The SD card is easiest to code, an EEPROM is the cheapest way to store data, and Wifi is the most convenient – but all have their downfalls. If we use an EEPROM or SD card, we need to physically obtain the device every time we want to transfer data from the device to a computer or server. Wifi would send the information over the network, but we need access to Wifi, which could potentially cost a lot of money. The ideal situation is sending the data to the cloud over a network connection of some sort with backup information being sent to an EEPROM and/or SD card.</p>
      </div>
      </div>
      </div>
