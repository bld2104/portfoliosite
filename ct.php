      <a data-toggle="collapse" data-parent="#accordion" href="#collapseFour">Where do you meet?</a>
      </h5>
      </div>
      <div id="collapseFour" class="panel-collapse collapse">
      <div class="panel-body">
      <p>Sometimes we meet at Code for Philly meetings, other times we meet separately. My group met at Temple University once, and once at a restaurant. It depends on which project you do and where they decide to meet. Code for Philly doesn’t dictate where your specific project must meet, they just provide space and food if you want to meet there.</p>
      </div>
