      </h5>
      </div>
      <div id="collapseSix" class="panel-collapse collapse">
      <div class="panel-body">
      <p>A few guys met at a SEPTA Hackathon and came up with the idea. They proposed it to the Clean Air Council, which was interested in backing the project. Josh Meyer is the current leader. Josh explained that this is the first time that this level of pollution detection will be collected across an entire city.</p>
